import { Schema, model } from "mongoose";

const teacherSchema = new Schema(
  {
    teacherId: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

export const Teacher = model("Teacher", teacherSchema);
